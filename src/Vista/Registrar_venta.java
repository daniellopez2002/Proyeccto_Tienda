//---------------------------BOTON_AGREGAR------------------------------
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String valor_v = jTextField3.getText();
        String id_v = jTextField5.getText();
        String id_p =jTextField4.getText();
        
        
        if (b.crear_conexion()){
            boolean venta = b.crear_venta(id_v, String.valueOf(user.id), id_p, valor_v);
            if (venta){
                JOptionPane.showConfirmDialog(rootPane, "SE REGISTRO UNA VENTA");
                id += 1;
                valor = 0;
            }else{
                JOptionPane.showConfirmDialog(rootPane, "NO SE PUDO REGISTRAR LA VENTA");
            }
                        
        }
    }//GEN-LAST:event_jButton4ActionPerformed


//------------------------------BOTON_AGREGAR-----------------------------------------
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String id_pro = jTextField4.getText();
        int cantidad = Integer.parseInt(jTextField2.getText());
        if(b.crear_conexion()){
            Producto p;
            try {
                p = b.buscarCodigoProducto(id_pro);
                p.setCantidad(cantidad);
                productos.add(p);   
            } catch (IOException ex) {
                Logger.getLogger(Registrar_venta.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        ValorTotal();
    }//GEN-LAST:event_jButton3ActionPerformed


